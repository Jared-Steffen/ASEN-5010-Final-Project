function [DCM] = MRPtoDCM(mrps)
% Maps MRPs to equivalent DCM

sigma1 = mrps(1);
sigma2 = mrps(2);
sigma3 = mrps(3);

sigma = norm(mrps);

DCM = 1/(1+sigma^2)^2 .* [ ...
    4*( sigma1^2 - sigma2^2 - sigma3^2 ) + (1 - sigma^2)^2, ...
    8*sigma1*sigma2 + 4*sigma3*(1 - sigma^2), ...
    8*sigma1*sigma3 - 4*sigma2*(1 - sigma^2); ...
    ...
    8*sigma2*sigma1 - 4*sigma3*(1 - sigma^2), ...
    4*(-sigma1^2 + sigma2^2 - sigma3^2) + (1 - sigma^2)^2, ...
    8*sigma2*sigma3 + 4*sigma1*(1 - sigma^2); ...
    ...
    8*sigma3*sigma1 + 4*sigma2*(1 - sigma^2), ...
    8*sigma3*sigma2 - 4*sigma1*(1 - sigma^2), ...
    4*(-sigma1^2 - sigma2^2 + sigma3^2) + (1 - sigma^2)^2 ...
];

end

